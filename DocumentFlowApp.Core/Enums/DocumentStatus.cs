namespace DocumentFlowApp.Core.Enums
{
    // Статусы документа в процессе жизненного цикла
    public enum DocumentStatus
    {
        Draft = 1,      // Черновик - документ создан но не отправлен на согласование
        InProgress = 2, // В работе - документ находится в процессе согласования
        Approved = 3,   // Утвержден - документ успешно прошел все этапы
        Rejected = 4,   // Отклонен - документ не прошел согласование
        Archived = 5    // В архиве - документ перемещен в архив
    }
}